
public class SimpleInterest {
	public static void main(String[] args) {
		// SI formula(principle*rate*time)/100

		double principle = 1300.00;
		int rate = 12;
		int time = 2;
		double SI=(principle*rate*time)/100;
		System.out.println(SI);

//***********OR**************************
		double principle1, rate1, time1, SI1;
		principle1 = 1300.00;
		rate1 = 12;
		time1 = 2;
		SI1 = (principle1 * rate1 * time1) / 100;
		System.out.println(SI1);

	}

}
